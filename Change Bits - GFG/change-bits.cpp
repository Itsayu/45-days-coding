class Solution {
  public:
    vector<int> changeBits(int N) {
        // code here
        int K=N;
        int cnt=0;
        while(N>0)
        {
            cnt++;
            N=N/2;
        }
        int a=pow(2,cnt)-1;
        int b=a-K;
        return {b,a};
    }
};

